package io.mersel.dss.agent.api.exceptions;

/**
 * GitHub Releases üzerinden güncelleme kontrolü başarısız olduğunda fırlatılır.
 *
 * <p>HTTP hataları, ağ timeout'ları, JSON parse hataları ve repo bulunamadı durumları bu exception
 * ile sarılır. {@link io.mersel.dss.agent.api.services.update.UpdateService} bunu yakalar ve {@code
 * Optional.empty()} ile geri döner — daemon ASLA bu exception yüzünden patlamaz.
 */
public class UpdateCheckException extends SignerException {
  private static final long serialVersionUID = 1L;

  public UpdateCheckException(String message) {
    super("UPDATE_CHECK_FAILED", message);
  }

  public UpdateCheckException(String message, Throwable cause) {
    super("UPDATE_CHECK_FAILED", message, cause);
  }
}

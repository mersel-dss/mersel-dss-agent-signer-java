package io.mersel.dss.agent.api.controllers;

import java.util.HashMap;
import java.util.Map;

import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;

import io.swagger.v3.oas.annotations.Operation;
import io.swagger.v3.oas.annotations.tags.Tag;

/**
 * Yerel daemon canlılığını kontrol için kullanılan basit endpoint. Eski projedeki {@code
 * /health/ping} davranışı korunmuştur.
 */
@RestController
@Tag(name = "Sağlık", description = "Yerel imza servisinin canlılık kontrolü.")
public class HealthController {

  @Operation(summary = "Servisin ayakta olduğunu doğrular.")
  @GetMapping("/health/ping")
  public ResponseEntity<Map<String, Object>> ping() {
    Map<String, Object> body = new HashMap<String, Object>();
    body.put("status", "UP");
    body.put("service", "mersel-dss-agent-signer");
    body.put("timestamp", System.currentTimeMillis());
    return ResponseEntity.ok(body);
  }
}
